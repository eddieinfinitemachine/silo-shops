const cityFilter = document.getElementById('cityFilter');
const categoryFilter = document.getElementById('categoryFilter');
const priceFilter = document.getElementById('priceFilter');
const searchInput = document.getElementById('searchInput');
const cardGrid = document.getElementById('cardGrid');
const resultsCount = document.getElementById('resultsCount');

const TOTAL_SHOPS = SHOPS.length;

function normalize(value) {
  return String(value || '').toLowerCase().trim();
}

function uniqueSorted(list) {
  return Array.from(new Set(list.filter(Boolean))).sort((a, b) =>
    a.localeCompare(b, 'en', { sensitivity: 'base' })
  );
}

function categoryParts(category) {
  return String(category || '')
    .split(',')
    .map((part) => part.trim())
    .filter(Boolean);
}

function formatInstagram(instagram) {
  const value = String(instagram || '').trim();
  if (!value) return null;
  if (/^https?:\/\//i.test(value)) {
    const pretty = value
      .replace(/^https?:\/\/(www\.)?/i, '')
      .replace(/\/$/, '');
    return { href: value, label: pretty };
  }

  const handle = value.replace(/^@/, '');
  return {
    href: 'https://instagram.com/' + handle,
    label: '@' + handle,
  };
}

function buildFilters() {
  const cities = uniqueSorted(SHOPS.map((shop) => shop.city));
  const categories = uniqueSorted(SHOPS.map((shop) => shop.category));
  const prices = uniqueSorted(SHOPS.map((shop) => shop.priceTier));

  for (const city of cities) {
    const option = document.createElement('option');
    option.value = city;
    option.textContent = city;
    cityFilter.appendChild(option);
  }

  for (const category of categories) {
    const option = document.createElement('option');
    option.value = category;
    option.textContent = category;
    categoryFilter.appendChild(option);
  }

  for (const price of prices) {
    const option = document.createElement('option');
    option.value = price;
    option.textContent = price;
    priceFilter.appendChild(option);
  }
}

function shopMatches(shop, selectedCity, selectedCategory, selectedPrice, searchTerm) {
  if (selectedCity && shop.city !== selectedCity) return false;
  if (selectedCategory && shop.category !== selectedCategory) return false;
  if (selectedPrice && shop.priceTier !== selectedPrice) return false;

  if (!searchTerm) return true;

  const haystack = normalize([
    shop.name,
    shop.city,
    shop.neighborhood,
    shop.vibe,
    shop.category,
    shop.address,
  ].join(' '));

  return haystack.includes(searchTerm);
}

function createCard(shop) {
  const card = document.createElement('article');
  card.className = 'card';

  const title = document.createElement('h2');
  title.className = 'shop-name';
  const titleLink = document.createElement('a');
  titleLink.textContent = shop.name || 'Unnamed Shop';
  titleLink.target = '_blank';
  titleLink.rel = 'noreferrer noopener';
  titleLink.href = shop.website || '#';
  if (!shop.website) {
    titleLink.removeAttribute('target');
    titleLink.removeAttribute('rel');
  }
  title.appendChild(titleLink);

  const location = document.createElement('p');
  location.className = 'location';
  const locationBits = [shop.city, shop.neighborhood].filter(Boolean);
  location.textContent = locationBits.join(' · ') || 'Location unavailable';

  const vibe = document.createElement('p');
  vibe.className = 'vibe';
  vibe.textContent = shop.vibe || 'No vibe notes available.';

  const tags = document.createElement('div');
  tags.className = 'tags';
  const pieces = categoryParts(shop.category);
  if (pieces.length === 0) pieces.push('Uncategorized');

  for (const piece of pieces) {
    const tag = document.createElement('span');
    tag.className = 'tag';
    tag.textContent = piece;
    tags.appendChild(tag);
  }

  const footer = document.createElement('div');
  footer.className = 'card-footer';

  const price = document.createElement('div');
  price.className = 'price';
  price.textContent = shop.priceTier || 'Price unknown';

  const links = document.createElement('div');
  links.className = 'links';

  const instaData = formatInstagram(shop.instagram);
  if (instaData) {
    const insta = document.createElement('a');
    insta.href = instaData.href;
    insta.target = '_blank';
    insta.rel = 'noreferrer noopener';
    insta.textContent = instaData.label;
    links.appendChild(insta);
  }

  if (shop.googleRating) {
    const rating = document.createElement('span');
    rating.className = 'rating';
    rating.textContent = 'Rating ' + shop.googleRating;
    links.appendChild(rating);
  }

  footer.appendChild(price);
  footer.appendChild(links);

  card.appendChild(title);
  card.appendChild(location);
  card.appendChild(vibe);
  card.appendChild(tags);
  card.appendChild(footer);

  return card;
}

function render() {
  const selectedCity = cityFilter.value;
  const selectedCategory = categoryFilter.value;
  const selectedPrice = priceFilter.value;
  const searchTerm = normalize(searchInput.value);

  const filtered = SHOPS.filter((shop) =>
    shopMatches(shop, selectedCity, selectedCategory, selectedPrice, searchTerm)
  );

  cardGrid.innerHTML = '';

  if (filtered.length === 0) {
    const empty = document.createElement('div');
    empty.className = 'empty';
    empty.textContent = 'No shops match the current filters.';
    cardGrid.appendChild(empty);
  } else {
    const fragment = document.createDocumentFragment();
    for (const shop of filtered) {
      fragment.appendChild(createCard(shop));
    }
    cardGrid.appendChild(fragment);
  }

  resultsCount.textContent = 'Showing ' + filtered.length + ' of ' + TOTAL_SHOPS + ' shops';
}

buildFilters();
render();

cityFilter.addEventListener('change', render);
categoryFilter.addEventListener('change', render);
priceFilter.addEventListener('change', render);
searchInput.addEventListener('input', render);
